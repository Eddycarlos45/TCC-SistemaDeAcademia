    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened

        try {
            //this.setExtendedState(MAXIMIZED_BOTH);
            //JFFormularioPrincipal.cadastro=false;
            Date dataSistema = new Date();
            SimpleDateFormat formato = new SimpleDateFormat("dd/MM/yyyy");
            jlData.setText(formato.format(dataSistema));

            Timer timer = new Timer(1000, new hora());
            timer.start();

            Funcionarios funci = new Funcionarios();
            DAOFuncionarios daofunci = new DAOFuncionarios();
            funci = daofunci.PesquisaLogin(acesso_funci);

            if (funci.getWallpaper_funci() != null) {
                ImageIcon plano_fundo = new ImageIcon((byte[]) funci.getWallpaper_funci());
                jLImagemHome.setIcon(new ImageIcon(plano_fundo.getImage().getScaledInstance(jLImagemHome.getWidth(), jLImagemHome.getHeight(), Image.SCALE_DEFAULT)));
            } else {

            }

            if (funci.getFoto_funcionario() != null) {
                ImageIcon perfil = new ImageIcon((byte[]) funci.getFoto_funcionario());

                jLFotoFunci.setIcon(new ImageIcon(perfil.getImage().getScaledInstance(jLFotoFunci.getWidth(), jLFotoFunci.getHeight(), Image.SCALE_DEFAULT)));
            }

            jLUsuario.setText(jLUsuario.getText() + acesso_funci.getNome());

            //CONFIGURA PERMISSÕES
            if (acesso_funci.getCargo().isGerenciar_aluno() == true) {
                jMIGerenciarAlunos.setEnabled(true);
            } else {
                jMIGerenciarAlunos.setEnabled(false);
            }

            if (acesso_funci.getCargo().isGerenciar_funci() == true) {

                jMIGerenciarFunci.setEnabled(true);
            } else {
                jMIGerenciarFunci.setEnabled(false);
            }

            if (acesso_funci.getCargo().isGerenciar_cargo() == true) {

                jMIGerenciarCargos.setEnabled(true);

            } else {
                jMIGerenciarCargos.setEnabled(false);
            }

            if (acesso_funci.getCargo().isGerenciar_permissoes() == true) {
                jMIGerenciarPermissoes.setEnabled(true);
            } else {
                jMIGerenciarPermissoes.setEnabled(false);
            }

            if (acesso_funci.getCargo().isGerenciar_relatorio() == true) {
                jMIRelatorioAlunos.setEnabled(true);
                jMIRelatorioFuncionarios.setEnabled(true);
                jMIRelatorioCargos.setEnabled(true);
                jMIRelatorioMensalidades.setEnabled(true);
            } else {
                jMIRelatorioAlunos.setEnabled(false);
                jMIRelatorioFuncionarios.setEnabled(false);
                jMIRelatorioCargos.setEnabled(false);
                jMIRelatorioMensalidades.setEnabled(false);
                
            }
            if (acesso_funci.getCargo().isConsulta_mensal() == true) {
                jMIConsultarMensal.setEnabled(true);
            } else {
                jMIConsultarMensal.setEnabled(false);
            }

        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(JFFormularioPrincipal.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_formWindowOpened

