    private void formWindowGainedFocus(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowGainedFocus
        // TODO add your handling code here:

        try {
            //ATUALIZA AS INFORMAÇÕES DE LOGIN

            DAOFuncionarios daofunci = new DAOFuncionarios();
            acesso_funci = daofunci.PesquisaLogin(acesso_funci);

            if (acesso_funci.getFoto_funcionario() != null) {
                ImageIcon foto_aluno = new ImageIcon(acesso_funci.getFoto_funcionario());
                jLFotoFunci.setIcon(new ImageIcon(foto_aluno.getImage().getScaledInstance(jLFotoFunci.getWidth(), jLFotoFunci.getHeight(), Image.SCALE_AREA_AVERAGING)));
            } else {

            }

            if (acesso_funci.getWallpaper_funci() != null) {
                ImageIcon plano_fundo = new ImageIcon((byte[]) acesso_funci.getWallpaper_funci());
                jLImagemHome.setIcon(new ImageIcon(plano_fundo.getImage().getScaledInstance(jLImagemHome.getWidth(), jLImagemHome.getHeight(), Image.SCALE_DEFAULT)));
            } else {

            }

            //CONFIGURA PERMISSÕES
            if (acesso_funci.getCargo().isGerenciar_aluno() == true) {
                jMIGerenciarAlunos.setEnabled(true);
            } else {
                jMIGerenciarAlunos.setEnabled(false);
            }

            if (acesso_funci.getCargo().isGerenciar_funci() == true) {

                jMIGerenciarFunci.setEnabled(true);
            } else {
                jMIGerenciarFunci.setEnabled(false);
            }

            if (acesso_funci.getCargo().isGerenciar_cargo() == true) {

                jMIGerenciarCargos.setEnabled(true);

            } else {
                jMIGerenciarCargos.setEnabled(false);
            }

            if (acesso_funci.getCargo().isGerenciar_permissoes() == true) {
                jMIGerenciarPermissoes.setEnabled(true);
            } else {
                jMIGerenciarPermissoes.setEnabled(false);
            }

//            if (acesso_funci.getCargo().isGerenciar_relatorio() == true) {
//                jMIConsultarRelatorios.setEnabled(true);
//            } else {
//                jMIConsultarRelatorios.setEnabled(false);
//            }
            if (acesso_funci.getCargo().isConsulta_mensal() == true) {
                jMIConsultarMensal.setEnabled(true);
            } else {
                jMIConsultarMensal.setEnabled(false);
            }

        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(JFFormularioPrincipal.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_formWindowGainedFocus

