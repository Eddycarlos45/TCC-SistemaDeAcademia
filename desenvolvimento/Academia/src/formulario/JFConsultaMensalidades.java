    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        try {

            Mensalidades mensal = new Mensalidades();
            mensal.setStatus_mensal((String) jCBListarMensal.getSelectedItem());

            if (jCBListarMensal.getSelectedItem().equals("Selecionar")) {
                if (jDCPeriodo.getDate() == null) {
                    DefaultTableModel modeloMensal = (DefaultTableModel) jTMensalidade.getModel();

                    modeloMensal.getDataVector().removeAllElements();
                    modeloMensal.addRow(new Object[]{"", "SELECIONE O PER√çODO", "SELECIONAR STATUS"});
                    
                } else {
                    DefaultTableModel modeloMensal = (DefaultTableModel) jTMensalidade.getModel();

                    modeloMensal.getDataVector().removeAllElements();
                    modeloMensal.addRow(new Object[]{"", "", "SELECIONAR STATUS"});
                }
            }

            if (jCBListarMensal.getSelectedItem().equals("Pago")) {

                if (jDCPeriodo.getDate() == null) {

                    DefaultTableModel modeloMensal = (DefaultTableModel) jTMensalidade.getModel();

                    modeloMensal.getDataVector().removeAllElements();
                    modeloMensal.addRow(new Object[]{"", "SELECIONE O PER√çODO", ""});

                } else {
                    populaTabela(mensal);

                }

            }

            if (jCBListarMensal.getSelectedItem().equals("Devendo")) {

                if (jDCPeriodo.getDate() == null) {

                    DefaultTableModel modeloMensal = (DefaultTableModel) jTMensalidade.getModel();

                    modeloMensal.getDataVector().removeAllElements();
                    modeloMensal.addRow(new Object[]{"", "SELECIONE O PER√çODO", ""});

                } else {
                    populaTabela(mensal);
                }

            }

            if (jCBListarMensal.getSelectedItem().equals("Todos")) {

                if (jDCPeriodo.getDate() == null) {

                    DefaultTableModel modeloMensal = (DefaultTableModel) jTMensalidade.getModel();

                    modeloMensal.getDataVector().removeAllElements();
                    modeloMensal.addRow(new Object[]{"", "SELECIONE O PER√çODO", ""});

                } else {
                    populaTabela2(mensal);
                }

            }

        } catch (SQLException | ClassNotFoundException | ParseException ex) {
            Logger.getLogger(JFConsultaMensalidades.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

