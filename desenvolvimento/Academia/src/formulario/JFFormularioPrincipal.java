    private void jMenuItem6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem6ActionPerformed
        // TODO add your handling code here:

        ///Redimensionar imagem
        JFileChooser escolha_arq = new JFileChooser();
        escolha_arq.setDialogTitle("Selecione a Foto do Aluno");
        escolha_arq.setFileSelectionMode(JFileChooser.FILES_ONLY);

        int opc = escolha_arq.showOpenDialog(this);

        if (opc == JFileChooser.APPROVE_OPTION) {
            try {
                File arquivo = new File("Caminho");
                arquivo = escolha_arq.getSelectedFile();
                String nome_arquivo = arquivo.getAbsolutePath();

                ImageIcon newwallpaper = new ImageIcon(nome_arquivo);
                jLImagemHome.setIcon(new ImageIcon(newwallpaper.getImage().getScaledInstance(jLImagemHome.getWidth(), jLImagemHome.getHeight(), Image.SCALE_AREA_AVERAGING)));

                Funcionarios funci = new Funcionarios();
                funci.setId_funcionario(acesso_funci.getId_funcionario());
                funci.setCaminho_wallpaper(nome_arquivo);

                DAOFuncionarios daofunci = new DAOFuncionarios();
                daofunci.novoWallpaper(funci);

                Funcionarios fun = new Funcionarios();
                fun = daofunci.PesquisaLogin(acesso_funci);

                acesso_funci = fun;

                ImageIcon wallpaper = new ImageIcon(acesso_funci.getWallpaper_funci());
                jLImagemHome.setIcon(new ImageIcon(wallpaper.getImage().getScaledInstance(jLImagemHome.getWidth(), jLImagemHome.getHeight(), Image.SCALE_AREA_AVERAGING)));

            } catch (ClassNotFoundException | SQLException ex) {
                Logger.getLogger(JFFormularioPrincipal.class.getName()).log(Level.SEVERE, null, ex);
            }

        }

    }//GEN-LAST:event_jMenuItem6ActionPerformed

