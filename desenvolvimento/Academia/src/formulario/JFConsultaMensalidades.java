    public void populaTabela(Mensalidades mensali) throws SQLException, ClassNotFoundException, ParseException {

        Mensalidades men = new Mensalidades();

        men.setData_vencimento(jDCPeriodo.getDate());
        men.setStatus_mensal(mensali.getStatus_mensal());

        DAOMensalidades daoMensal = new DAOMensalidades();

        List<Mensalidades> resultado;
        resultado = daoMensal.ListarStatus(men);

        DefaultTableModel modelo = (DefaultTableModel) jTMensalidade.getModel();
        modelo.getDataVector().removeAllElements();

        if (resultado.isEmpty() == true) {
            modelo.getDataVector().removeAllElements();
            modelo.addRow(new Object[]{"VAZIO", "VAZIO", "VAZIO"});
        } else {

            modelo.getDataVector().removeAllElements();
            for (int i = 0; i < resultado.size(); i++) {
                modelo.addRow(new Object[]{resultado.get(i).getAluno().getNome_aluno(),
                    resultado.get(i).getData_vencimento(),
                    resultado.get(i).getStatus_mensal()});
            }
        }
    }

    public void populaTabela2(Mensalidades mensali) throws SQLException, ClassNotFoundException, ParseException {

        Mensalidades men = new Mensalidades();

        men.setData_vencimento(jDCPeriodo.getDate());

        DAOMensalidades daoMensal = new DAOMensalidades();

        List<Mensalidades> resultado;
        resultado = daoMensal.ListarStatus2(men);

        DefaultTableModel modelo = (DefaultTableModel) jTMensalidade.getModel();
        modelo.getDataVector().removeAllElements();

        if (resultado.isEmpty() == true) {
            modelo.getDataVector().removeAllElements();
            modelo.addRow(new Object[]{"VAZIO", "VAZIO", "VAZIO"});
        } else {

            modelo.getDataVector().removeAllElements();
            for (int i = 0; i < resultado.size(); i++) {
                modelo.addRow(new Object[]{
                    resultado.get(i).getAluno().getNome_aluno(),
                    resultado.get(i).getData_vencimento(),
                    resultado.get(i).getStatus_mensal()});
            }
        }
    }

